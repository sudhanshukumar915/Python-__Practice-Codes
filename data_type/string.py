#Subscripting on string or character
text="Sudhanshu"
print(text[0])
print(text[2])
print(text[4])
print(text[-1])
print(text[-3])

#Subscripting on list
number=[10,20,30,40,50]
print(number[1])
print(number[2])
print(number[3])
print(number[-1])



#Subscripting on tuple
data=("Apple","Banana","Orange","gauva","Pineapple","Mango")
print(data[0])
print(data[2])
print(data[3])
print(data[-1])

#Subscripting on directory
student={"name":"Sudhanshu","Roll":"235164","College":"LNMCBM","Section":"C"}
print(student["name"])
print(student["Roll"])
print(student["College"])
print(student["Section"])

#Integer Data_type
print(123+345)
print(123_456_789)

#Float Data_type
print(3.1132)

#Boolean Data_type
print(True)
print(False)
